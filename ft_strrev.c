#include "libft.h"

char		*ft_strrev(char *s)
{
	char	tmp;
	int		i;
	int		y;

	i = 0;
	y = 0;
	while (s[i] != 0)
	{
		i++;
	}
	while (i > y)
	{
		i--;
		tmp = s[i];
		s[i] = s[y];
		s[y] = tmp;
		y++;
	}
	return (s);
}
